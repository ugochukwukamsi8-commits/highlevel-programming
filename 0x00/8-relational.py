#!/usr/bin/python3
"""a module that handles all the relational operators"""
num1 = int(input("Enter the first number;"))
num2 = int(input("Enter the second nuumber;"))

print(f"Greater than {num1} > {num2} = {num1>num2}")
print(f"Greater than or equal to {num1} >= {num2} = {num1>=num2}")
print(f"Less than or equal to {num1} <= {num2} = {num1<=num2}")
print(f"Less than {num1} < {num2} = {num1<num2}")
print(f"Equal to  {num1} == {num2} = {num1==num2}")
print(f"Not equal to  {num1} != {num2} = {num1!=num2}")
